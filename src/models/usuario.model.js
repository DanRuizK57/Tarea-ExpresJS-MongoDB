import mongoose from 'mongoose';

const usuarioSchema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
    },
    dni: {
      type: String,
      required: true,
    },
    password: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);

const UsuarioModel = mongoose.model('Usuario', usuarioSchema, 'usuarios');

export default UsuarioModel;
